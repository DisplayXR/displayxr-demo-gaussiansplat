// ── Uniform buffer layout (std140, matches preprocess.comp) ──────────────
struct alignas(16) GsUniformBuffer {
    float camera_position[4];  // vec4, offset 0
    float proj_mat[16];        // mat4, offset 16
    float view_mat[16];        // mat4, offset 80
    uint32_t width;            // offset 144
    uint32_t height;           // offset 148
    float tan_fovx;            // offset 152
    float tan_fovy;            // offset 156
    float clip_far;            // offset 160 — view-space far cull (0 = disabled)
    float clip_fade_frac;      // offset 164 — soft NEAR-clip band as fraction of clip_near (0 = hard cut; far is always hard)
    float clip_near;           // offset 168 — view-space near cull (0 = disabled)
};
static_assert(sizeof(GsUniformBuffer) == 176, "GsUniformBuffer must be 176 bytes");

// ── Helper: create a compute pipeline from SPIR-V ───────────────────────
static VkShaderModule createShaderModule(VkDevice device, const uint32_t* code, size_t sizeBytes)
{
    VkShaderModuleCreateInfo ci = {VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO};
    ci.codeSize = sizeBytes;
    ci.pCode = code;
    VkShaderModule mod = VK_NULL_HANDLE;
    vkCreateShaderModule(device, &ci, nullptr, &mod);
    return mod;
}

static VkPipeline createComputePipeline(VkDevice device,
                                        VkPipelineLayout layout,
                                        VkShaderModule shader,
                                        const VkSpecializationInfo* specInfo = nullptr)
{
    VkComputePipelineCreateInfo ci = {VK_STRUCTURE_TYPE_COMPUTE_PIPELINE_CREATE_INFO};
    ci.stage.sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
    ci.stage.stage = VK_SHADER_STAGE_COMPUTE_BIT;
    ci.stage.module = shader;
    ci.stage.pName = "main";
    ci.stage.pSpecializationInfo = specInfo;
    ci.layout = layout;

    VkPipeline pipe = VK_NULL_HANDLE;
    vkCreateComputePipelines(device, VK_NULL_HANDLE, 1, &ci, nullptr, &pipe);
    return pipe;
}

// ── Helper: create descriptor set layout ─────────────────────────────────
static VkDescriptorSetLayout createDSLayout(VkDevice device,
                                            const std::vector<VkDescriptorType>& types)
{
    std::vector<VkDescriptorSetLayoutBinding> bindings(types.size());
    for (size_t i = 0; i < types.size(); i++) {
        bindings[i] = {};
        bindings[i].binding = (uint32_t)i;
        bindings[i].descriptorType = types[i];
        bindings[i].descriptorCount = 1;
        bindings[i].stageFlags = VK_SHADER_STAGE_COMPUTE_BIT;
    }
    VkDescriptorSetLayoutCreateInfo ci = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO};
    ci.bindingCount = (uint32_t)bindings.size();
    ci.pBindings = bindings.data();
    VkDescriptorSetLayout layout = VK_NULL_HANDLE;
    vkCreateDescriptorSetLayout(device, &ci, nullptr, &layout);
    return layout;
}

// ── Helper: create pipeline layout ───────────────────────────────────────
static VkPipelineLayout createPipeLayout(VkDevice device,
                                         const std::vector<VkDescriptorSetLayout>& dsLayouts,
                                         uint32_t pushSize)
{
    VkPushConstantRange pcr = {};
    pcr.stageFlags = VK_SHADER_STAGE_COMPUTE_BIT;
    pcr.offset = 0;
    pcr.size = pushSize;

    VkPipelineLayoutCreateInfo ci = {VK_STRUCTURE_TYPE_PIPELINE_LAYOUT_CREATE_INFO};
    ci.setLayoutCount = (uint32_t)dsLayouts.size();
    ci.pSetLayouts = dsLayouts.data();
    if (pushSize > 0) {
        ci.pushConstantRangeCount = 1;
        ci.pPushConstantRanges = &pcr;
    }
    VkPipelineLayout layout = VK_NULL_HANDLE;
    vkCreatePipelineLayout(device, &ci, nullptr, &layout);
    return layout;
}

// ── Helper: allocate descriptor set ──────────────────────────────────────
static VkDescriptorSet allocDS(VkDevice device, VkDescriptorPool pool,
                               VkDescriptorSetLayout layout)
{
    VkDescriptorSetAllocateInfo ai = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO};
    ai.descriptorPool = pool;
    ai.descriptorSetCount = 1;
    ai.pSetLayouts = &layout;
    VkDescriptorSet ds = VK_NULL_HANDLE;
    vkAllocateDescriptorSets(device, &ai, &ds);
    return ds;
}

// ── Helper: write buffer descriptor ──────────────────────────────────────
static void writeBufferDS(VkDevice device, VkDescriptorSet ds, uint32_t binding,
                          VkDescriptorType type, VkBuffer buffer, VkDeviceSize size)
{
    VkDescriptorBufferInfo bi = {};
    bi.buffer = buffer;
    bi.offset = 0;
    bi.range = size;

    VkWriteDescriptorSet w = {VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET};
    w.dstSet = ds;
    w.dstBinding = binding;
    w.descriptorCount = 1;
    w.descriptorType = type;
    w.pBufferInfo = &bi;
    vkUpdateDescriptorSets(device, 1, &w, 0, nullptr);
}

// ── Helper: write image descriptor ───────────────────────────────────────
static void writeImageDS(VkDevice device, VkDescriptorSet ds, uint32_t binding,
                         VkImageView view)
{
    VkDescriptorImageInfo ii = {};
    ii.imageView = view;
    ii.imageLayout = VK_IMAGE_LAYOUT_GENERAL;

    VkWriteDescriptorSet w = {VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET};
    w.dstSet = ds;
    w.dstBinding = binding;
    w.descriptorCount = 1;
    w.descriptorType = VK_DESCRIPTOR_TYPE_STORAGE_IMAGE;
    w.pImageInfo = &ii;
    vkUpdateDescriptorSets(device, 1, &w, 0, nullptr);
}

// ═════════════════════════════════════════════════════════════════════════
// init
// ═════════════════════════════════════════════════════════════════════════

bool GsRenderer::init(VkInstance instance,
                      VkPhysicalDevice physicalDevice,
                      VkDevice device,
                      VkQueue queue,
                      uint32_t queueFamilyIndex,
                      uint32_t renderWidth,
                      uint32_t renderHeight)
{
    instance_ = instance;
    physDevice_ = physicalDevice;
    device_ = device;
    queue_ = queue;
    queueFamily_ = queueFamilyIndex;
    width_ = renderWidth;
    height_ = renderHeight;

    // Query subgroup size
    VkPhysicalDeviceSubgroupProperties subgroupProps = {
        VK_STRUCTURE_TYPE_PHYSICAL_DEVICE_SUBGROUP_PROPERTIES};
    VkPhysicalDeviceProperties2 props2 = {VK_STRUCTURE_TYPE_PHYSICAL_DEVICE_PROPERTIES_2};
    props2.pNext = &subgroupProps;
    vkGetPhysicalDeviceProperties2(instance_ ? physDevice_ : VK_NULL_HANDLE, &props2);
    subgroupSize_ = subgroupProps.subgroupSize;
    if (subgroupSize_ == 0) subgroupSize_ = 32;

    // GPU timestamp support: timestampPeriod (ns/tick) is a device limit, but
    // timestamps only work on a queue whose family reports timestampValidBits>0.
    timestampPeriod_ = props2.properties.limits.timestampPeriod;
    {
        uint32_t qfCount = 0;
        vkGetPhysicalDeviceQueueFamilyProperties(physDevice_, &qfCount, nullptr);
        std::vector<VkQueueFamilyProperties> qfProps(qfCount);
        vkGetPhysicalDeviceQueueFamilyProperties(physDevice_, &qfCount, qfProps.data());
        if (queueFamilyIndex < qfCount)
            tsValidBits_ = qfProps[queueFamilyIndex].timestampValidBits;
    }

    // Tile dimensions
    tileX_ = (width_ + 15) / 16;
    tileY_ = (height_ + 15) / 16;

    // The 32-bit sort key carries the tile index in its high 16 bits
    // (preprocess_sort.comp), so the tile grid must stay within 65536 tiles
    // (= 16.7 Mpx per eye at 16x16 tiles — far above any current canvas).
    if ((uint64_t)tileX_ * (uint64_t)tileY_ > 65536) {
        fprintf(stderr,
                "GsRenderer: %ux%u px = %u tiles exceeds the 16-bit sort tile "
                "index (max 65536 tiles = 16.7 Mpx/eye)\n",
                width_, height_, tileX_ * tileY_);
        return false;
    }

    // Create command pool
    VkCommandPoolCreateInfo poolInfo = {VK_STRUCTURE_TYPE_COMMAND_POOL_CREATE_INFO};
    poolInfo.flags = VK_COMMAND_POOL_CREATE_RESET_COMMAND_BUFFER_BIT;
    poolInfo.queueFamilyIndex = queueFamilyIndex;
    VkResult res = vkCreateCommandPool(device, &poolInfo, nullptr, &cmdPool_);
    if (res != VK_SUCCESS) {
        fprintf(stderr, "GsRenderer: failed to create command pool (%d)\n", res);
        return false;
    }

    // Timestamp query pool (one pool of kNumTimestamps, reset+reused per eye).
    // Logging is throttled (one line per kTsLogPeriod renderEye calls) and
    // auto-disabled when the queue lacks timestamp support.
    if (timestampPeriod_ > 0.0f && tsValidBits_ > 0) {
        VkQueryPoolCreateInfo qpci = {VK_STRUCTURE_TYPE_QUERY_POOL_CREATE_INFO};
        qpci.queryType = VK_QUERY_TYPE_TIMESTAMP;
        qpci.queryCount = kNumTimestamps;
        if (vkCreateQueryPool(device_, &qpci, nullptr, &tsPool_) != VK_SUCCESS) {
            tsPool_ = VK_NULL_HANDLE;
            timestampPeriod_ = 0.0f;  // disable profiling
        }
    } else {
        timestampPeriod_ = 0.0f;  // disable profiling
    }

    initialized_ = true;
    GS_LOGI("GsRenderer: initialized (%ux%u, subgroup=%u, tiles=%ux%u, ts_period=%.2fns valid_bits=%u)",
            width_, height_, subgroupSize_, tileX_, tileY_, timestampPeriod_, tsValidBits_);
    return true;
}

// ═════════════════════════════════════════════════════════════════════════
// createPipelines — build all 8 compute pipelines
// ═════════════════════════════════════════════════════════════════════════

bool GsRenderer::createPipelines()
{
    auto S = VK_DESCRIPTOR_TYPE_STORAGE_BUFFER;
    auto U = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER;
    auto I = VK_DESCRIPTOR_TYPE_STORAGE_IMAGE;

    // 1. precomp_cov3d: set0 = {SSBO, SSBO}, push = float
    dslPrecompCov3d_ = createDSLayout(device_, {S, S});
    layoutPrecompCov3d_ = createPipeLayout(device_, {dslPrecompCov3d_}, sizeof(float));
    {
        auto mod = createShaderModule(device_, precomp_cov3d_comp_data,
                                      sizeof(precomp_cov3d_comp_data));
        pipePrecompCov3d_ = createComputePipeline(device_, layoutPrecompCov3d_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 2. preprocess: set0 = {SSBO, SSBO}, set1 = {UBO, SSBO, SSBO}
    dslPreprocessSet0_ = createDSLayout(device_, {S, S});
    dslPreprocessSet1_ = createDSLayout(device_, {U, S, S});
    layoutPreprocess_ = createPipeLayout(device_,
        {dslPreprocessSet0_, dslPreprocessSet1_}, 0);
    {
        auto mod = createShaderModule(device_, preprocess_comp_data,
                                      sizeof(preprocess_comp_data));
        pipePreprocess_ = createComputePipeline(device_, layoutPreprocess_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 3. prefix_sum: set0 = {SSBO, SSBO}, push = uint
    dslPrefixSum_ = createDSLayout(device_, {S, S});
    layoutPrefixSum_ = createPipeLayout(device_, {dslPrefixSum_}, sizeof(uint32_t));
    {
        auto mod = createShaderModule(device_, prefix_sum_comp_data,
                                      sizeof(prefix_sum_comp_data));
        pipePrefixSum_ = createComputePipeline(device_, layoutPrefixSum_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 4. preprocess_sort: set0 = {SSBO, SSBO, SSBO, SSBO},
    // push = {uint tileX, float depthMin, float invDepthRange}
    dslPreprocessSort_ = createDSLayout(device_, {S, S, S, S});
    layoutPreprocessSort_ = createPipeLayout(device_, {dslPreprocessSort_}, 3 * sizeof(uint32_t));
    {
        auto mod = createShaderModule(device_, preprocess_sort_comp_data,
                                      sizeof(preprocess_sort_comp_data));
        pipePreprocessSort_ = createComputePipeline(device_, layoutPreprocessSort_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 5. hist: set0 = {SSBO, SSBO}, push = 4*uint
    dslHist_ = createDSLayout(device_, {S, S});
    layoutHist_ = createPipeLayout(device_, {dslHist_}, 4 * sizeof(uint32_t));
    {
        auto mod = createShaderModule(device_, hist_comp_data,
                                      sizeof(hist_comp_data));
        pipeHist_ = createComputePipeline(device_, layoutHist_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 6. sort: set0 = {SSBO, SSBO, SSBO, SSBO, SSBO}, push = 4*uint
    //    Specialization constant 0 = subgroupSize
    dslSort_ = createDSLayout(device_, {S, S, S, S, S});
    layoutSort_ = createPipeLayout(device_, {dslSort_}, 4 * sizeof(uint32_t));
    {
        auto mod = createShaderModule(device_, sort_comp_data,
                                      sizeof(sort_comp_data));
        VkSpecializationMapEntry specEntry = {};
        specEntry.constantID = 0;
        specEntry.offset = 0;
        specEntry.size = sizeof(uint32_t);
        VkSpecializationInfo specInfo = {};
        specInfo.mapEntryCount = 1;
        specInfo.pMapEntries = &specEntry;
        specInfo.dataSize = sizeof(uint32_t);
        specInfo.pData = &subgroupSize_;
        pipeSort_ = createComputePipeline(device_, layoutSort_, mod, &specInfo);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 7. tile_boundary: set0 = {SSBO, SSBO}, push = uint
    dslTileBoundary_ = createDSLayout(device_, {S, S});
    layoutTileBoundary_ = createPipeLayout(device_, {dslTileBoundary_}, sizeof(uint32_t));
    {
        auto mod = createShaderModule(device_, tile_boundary_comp_data,
                                      sizeof(tile_boundary_comp_data));
        pipeTileBoundary_ = createComputePipeline(device_, layoutTileBoundary_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // 8. render: set0 = {SSBO, SSBO, SSBO}, set1 = {storage image}, push = 4*uint
    // (width, height, apply_srgb, transparent_bg)
    dslRenderSet0_ = createDSLayout(device_, {S, S, S});
    dslRenderSet1_ = createDSLayout(device_, {I});
    layoutRender_ = createPipeLayout(device_,
        {dslRenderSet0_, dslRenderSet1_}, 4 * sizeof(uint32_t));
    {
        auto mod = createShaderModule(device_, render_comp_data,
                                      sizeof(render_comp_data));
        pipeRender_ = createComputePipeline(device_, layoutRender_, mod);
        vkDestroyShaderModule(device_, mod, nullptr);
    }

    // Launch gracefully when a pipeline can't link (e.g. a driver rejecting a
    // shader feature) instead of crashing on the first dispatch.
    const VkPipeline pipes[8] = {pipePrecompCov3d_, pipePreprocess_, pipePrefixSum_,
                                 pipePreprocessSort_, pipeHist_,     pipeSort_,
                                 pipeTileBoundary_,   pipeRender_};
    for (int i = 0; i < 8; ++i) {
        if (pipes[i] == VK_NULL_HANDLE) {
            fprintf(stderr, "GsRenderer: compute pipeline %d failed to create\n", i);
            return false;
        }
    }

    printf("GsRenderer: 8 compute pipelines created\n");
    return true;
}

// ═════════════════════════════════════════════════════════════════════════
// createBuffers — allocate all GPU buffers
// ═════════════════════════════════════════════════════════════════════════

bool GsRenderer::createBuffers()
{
    uint32_t N = numGaussians_;
    uint32_t totalTiles = tileX_ * tileY_;
    // Initial multiplier sized for mono full-resolution viewport. SBS/3D modes
    // generate ~4× fewer fragments per gaussian (smaller pixel-space radius),
    // so this is generous for stereo. growSortBuffers() handles overflow.
    maxSortInstances_ = N * 8;
    if (maxSortInstances_ < totalTiles)
        maxSortInstances_ = totalTiles;

    auto devLocal = VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT;
    auto hostVis = (VkMemoryPropertyFlags)(VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT |
                                           VK_MEMORY_PROPERTY_HOST_COHERENT_BIT);
    auto ssboUsage = (VkBufferUsageFlags)(VK_BUFFER_USAGE_STORAGE_BUFFER_BIT |
                                          VK_BUFFER_USAGE_TRANSFER_DST_BIT |
                                          VK_BUFFER_USAGE_TRANSFER_SRC_BIT);

    // Vertex data (uploaded via staging)
    vertexBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)N * 240, ssboUsage, devLocal);

    // Cov3D (written by precomp_cov3d)
    cov3DBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)N * 24, ssboUsage, devLocal);

    // Uniform buffer (host-visible, mapped each frame)
    uniformBuffer_ = gsCreateBuffer(device_, physDevice_,
        sizeof(GsUniformBuffer),
        VK_BUFFER_USAGE_UNIFORM_BUFFER_BIT,
        hostVis);

    // Vertex attributes (written by preprocess, read by preprocess_sort + render)
    vertexAttrBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)N * 64, ssboUsage, devLocal);

    // Tile overlap counts (written by preprocess, copied to prefix ping)
    tileOverlapBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)N * 4, ssboUsage, devLocal);

    // Prefix sum ping/pong buffers
    prefixSumPingBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)N * 4, ssboUsage, devLocal);
    prefixSumPongBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)N * 4, ssboUsage, devLocal);

    // Total sum readback (host-visible)
    totalSumHostBuffer_ = gsCreateBuffer(device_, physDevice_,
        sizeof(uint32_t),
        VK_BUFFER_USAGE_TRANSFER_DST_BIT,
        hostVis);

    // Sort key/value buffers (32-bit packed keys — see preprocess_sort.comp)
    sortKeysEvenBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)maxSortInstances_ * 4, ssboUsage, devLocal);
    sortKeysOddBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)maxSortInstances_ * 4, ssboUsage, devLocal);
    sortValsEvenBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)maxSortInstances_ * 4, ssboUsage, devLocal);
    sortValsOddBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)maxSortInstances_ * 4, ssboUsage, devLocal);

    // Sort histogram
    // numWorkgroups for radix sort:
    uint32_t globalInv = (maxSortInstances_ + numRadixSortBlocksPerWG_ * 256 - 1) /
                         (numRadixSortBlocksPerWG_ * 256);
    numSortWorkgroups_ = globalInv;
    if (numSortWorkgroups_ == 0) numSortWorkgroups_ = 1;
    sortHistBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)numSortWorkgroups_ * 256 * 4, ssboUsage, devLocal);

    // Tile boundary
    tileBoundaryBuffer_ = gsCreateBuffer(device_, physDevice_,
        (VkDeviceSize)tileX_ * tileY_ * 2 * 4, ssboUsage, devLocal);

    // Internal render image
    renderImage_ = gsCreateImage2D(device_, physDevice_, width_, height_,
        VK_FORMAT_R8G8B8A8_UNORM,
        VK_IMAGE_USAGE_STORAGE_BIT | VK_IMAGE_USAGE_TRANSFER_SRC_BIT);

    printf("GsRenderer: buffers allocated (N=%u, sortCap=%u, hist_wg=%u)\n",
           N, maxSortInstances_, numSortWorkgroups_);
    return true;
}

// ═════════════════════════════════════════════════════════════════════════
// createDescriptorSets
// ═════════════════════════════════════════════════════════════════════════

bool GsRenderer::createDescriptorSets()
{
    // Descriptor pool — count all needed descriptors
    VkDescriptorPoolSize poolSizes[] = {
        {VK_DESCRIPTOR_TYPE_STORAGE_BUFFER, 40},
        {VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER, 2},
        {VK_DESCRIPTOR_TYPE_STORAGE_IMAGE, 2},
    };
    VkDescriptorPoolCreateInfo pci = {VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO};
    pci.maxSets = 16;
    pci.poolSizeCount = 3;
    pci.pPoolSizes = poolSizes;
    if (vkCreateDescriptorPool(device_, &pci, nullptr, &descriptorPool_) != VK_SUCCESS) {
        fprintf(stderr, "GsRenderer: failed to create descriptor pool\n");
        return false;
    }

    uint32_t N = numGaussians_;

    // 1. precomp_cov3d set0: vertices + cov3d
    dsPrecompCov3d_ = allocDS(device_, descriptorPool_, dslPrecompCov3d_);
    writeBufferDS(device_, dsPrecompCov3d_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  vertexBuffer_.buffer, vertexBuffer_.size);
    writeBufferDS(device_, dsPrecompCov3d_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  cov3DBuffer_.buffer, cov3DBuffer_.size);

    // 2. preprocess set0: vertices + cov3d (same data as precomp)
    dsPreprocessSet0_ = allocDS(device_, descriptorPool_, dslPreprocessSet0_);
    writeBufferDS(device_, dsPreprocessSet0_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  vertexBuffer_.buffer, vertexBuffer_.size);
    writeBufferDS(device_, dsPreprocessSet0_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  cov3DBuffer_.buffer, cov3DBuffer_.size);

    // 3. preprocess set1: uniform + vertexAttrs + tileOverlap
    dsPreprocessSet1_ = allocDS(device_, descriptorPool_, dslPreprocessSet1_);
    writeBufferDS(device_, dsPreprocessSet1_, 0, VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER,
                  uniformBuffer_.buffer, uniformBuffer_.size);
    writeBufferDS(device_, dsPreprocessSet1_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  vertexAttrBuffer_.buffer, vertexAttrBuffer_.size);
    writeBufferDS(device_, dsPreprocessSet1_, 2, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  tileOverlapBuffer_.buffer, tileOverlapBuffer_.size);

    // 4. prefix_sum set0: ping + pong
    dsPrefixSum_ = allocDS(device_, descriptorPool_, dslPrefixSum_);
    writeBufferDS(device_, dsPrefixSum_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  prefixSumPingBuffer_.buffer, prefixSumPingBuffer_.size);
    writeBufferDS(device_, dsPrefixSum_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  prefixSumPongBuffer_.buffer, prefixSumPongBuffer_.size);

    // 5. preprocess_sort set0: vertexAttrs + prefixResult + keysEven + valsEven
    //    The prefix sum result location depends on iteration parity.
    //    We bind the correct buffer here based on numPrefixSumIter_.
    dsPreprocessSort_ = allocDS(device_, descriptorPool_, dslPreprocessSort_);
    writeBufferDS(device_, dsPreprocessSort_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  vertexAttrBuffer_.buffer, vertexAttrBuffer_.size);
    {
        // Result is in pong if numPrefixSumIter_ is odd, ping if even
        VkBuffer prefixResult = (numPrefixSumIter_ % 2 == 1) ?
            prefixSumPongBuffer_.buffer : prefixSumPingBuffer_.buffer;
        VkDeviceSize prefixSize = (VkDeviceSize)N * 4;
        writeBufferDS(device_, dsPreprocessSort_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                      prefixResult, prefixSize);
    }
    writeBufferDS(device_, dsPreprocessSort_, 2, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysEvenBuffer_.buffer, sortKeysEvenBuffer_.size);
    writeBufferDS(device_, dsPreprocessSort_, 3, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortValsEvenBuffer_.buffer, sortValsEvenBuffer_.size);

    // 6. hist — two descriptor sets for even/odd sort passes
    dsHistEven_ = allocDS(device_, descriptorPool_, dslHist_);
    writeBufferDS(device_, dsHistEven_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysEvenBuffer_.buffer, sortKeysEvenBuffer_.size);
    writeBufferDS(device_, dsHistEven_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortHistBuffer_.buffer, sortHistBuffer_.size);

    dsHistOdd_ = allocDS(device_, descriptorPool_, dslHist_);
    writeBufferDS(device_, dsHistOdd_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysOddBuffer_.buffer, sortKeysOddBuffer_.size);
    writeBufferDS(device_, dsHistOdd_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortHistBuffer_.buffer, sortHistBuffer_.size);

    // 7. sort — two descriptor sets for even→odd and odd→even
    dsSortEvenToOdd_ = allocDS(device_, descriptorPool_, dslSort_);
    writeBufferDS(device_, dsSortEvenToOdd_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysEvenBuffer_.buffer, sortKeysEvenBuffer_.size);
    writeBufferDS(device_, dsSortEvenToOdd_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysOddBuffer_.buffer, sortKeysOddBuffer_.size);
    writeBufferDS(device_, dsSortEvenToOdd_, 2, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortValsEvenBuffer_.buffer, sortValsEvenBuffer_.size);
    writeBufferDS(device_, dsSortEvenToOdd_, 3, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortValsOddBuffer_.buffer, sortValsOddBuffer_.size);
    writeBufferDS(device_, dsSortEvenToOdd_, 4, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortHistBuffer_.buffer, sortHistBuffer_.size);

    dsSortOddToEven_ = allocDS(device_, descriptorPool_, dslSort_);
    writeBufferDS(device_, dsSortOddToEven_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysOddBuffer_.buffer, sortKeysOddBuffer_.size);
    writeBufferDS(device_, dsSortOddToEven_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysEvenBuffer_.buffer, sortKeysEvenBuffer_.size);
    writeBufferDS(device_, dsSortOddToEven_, 2, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortValsOddBuffer_.buffer, sortValsOddBuffer_.size);
    writeBufferDS(device_, dsSortOddToEven_, 3, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortValsEvenBuffer_.buffer, sortValsEvenBuffer_.size);
    writeBufferDS(device_, dsSortOddToEven_, 4, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortHistBuffer_.buffer, sortHistBuffer_.size);

    // 8. tile_boundary: sortedKeysEven + boundaries
    dsTileBoundary_ = allocDS(device_, descriptorPool_, dslTileBoundary_);
    writeBufferDS(device_, dsTileBoundary_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortKeysEvenBuffer_.buffer, sortKeysEvenBuffer_.size);
    writeBufferDS(device_, dsTileBoundary_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  tileBoundaryBuffer_.buffer, tileBoundaryBuffer_.size);

    // 9. render set0: vertexAttrs + boundaries + sortedValsEven
    dsRenderSet0_ = allocDS(device_, descriptorPool_, dslRenderSet0_);
    writeBufferDS(device_, dsRenderSet0_, 0, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  vertexAttrBuffer_.buffer, vertexAttrBuffer_.size);
    writeBufferDS(device_, dsRenderSet0_, 1, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  tileBoundaryBuffer_.buffer, tileBoundaryBuffer_.size);
    writeBufferDS(device_, dsRenderSet0_, 2, VK_DESCRIPTOR_TYPE_STORAGE_BUFFER,
                  sortValsEvenBuffer_.buffer, sortValsEvenBuffer_.size);

    // 10. render set1: output image
    dsRenderSet1_ = allocDS(device_, descriptorPool_, dslRenderSet1_);
    writeImageDS(device_, dsRenderSet1_, 0, renderImage_.view);

    printf("GsRenderer: descriptor sets created\n");
    return true;
}

// ═════════════════════════════════════════════════════════════════════════
// dispatchPrecompCov3d — one-time 3D covariance precomputation
// ═════════════════════════════════════════════════════════════════════════

void GsRenderer::dispatchPrecompCov3d()
{
    VkCommandBufferAllocateInfo ai = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO};
    ai.commandPool = cmdPool_;
    ai.level = VK_COMMAND_BUFFER_LEVEL_PRIMARY;
    ai.commandBufferCount = 1;
    VkCommandBuffer cmd;
    vkAllocateCommandBuffers(device_, &ai, &cmd);

    VkCommandBufferBeginInfo bi = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO};
    bi.flags = VK_COMMAND_BUFFER_USAGE_ONE_TIME_SUBMIT_BIT;
    vkBeginCommandBuffer(cmd, &bi);

    vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_COMPUTE, pipePrecompCov3d_);
    vkCmdBindDescriptorSets(cmd, VK_PIPELINE_BIND_POINT_COMPUTE,
        layoutPrecompCov3d_, 0, 1, &dsPrecompCov3d_, 0, nullptr);

    float scaleFactor = 1.0f;
    vkCmdPushConstants(cmd, layoutPrecompCov3d_, VK_SHADER_STAGE_COMPUTE_BIT,
                       0, sizeof(float), &scaleFactor);

    uint32_t groups = (numGaussians_ + 255) / 256;
    vkCmdDispatch(cmd, groups, 1, 1);

    vkEndCommandBuffer(cmd);

    VkSubmitInfo si = {VK_STRUCTURE_TYPE_SUBMIT_INFO};
    si.commandBufferCount = 1;
    si.pCommandBuffers = &cmd;
    vkQueueSubmit(queue_, 1, &si, VK_NULL_HANDLE);
    vkQueueWaitIdle(queue_);
    vkFreeCommandBuffers(device_, cmdPool_, 1, &cmd);

    printf("GsRenderer: precomp_cov3d dispatched (%u groups)\n", groups);
}

// ═════════════════════════════════════════════════════════════════════════
// loadScene
// ═════════════════════════════════════════════════════════════════════════

bool GsRenderer::loadScene(const char* plyPath)
{
    if (!initialized_) {
        fprintf(stderr, "GsRenderer: not initialized\n");
        return false;
    }

    // Clean up previous scene if any
    cleanupScene();

    // Auto-detect format by extension and parse scene file
    std::vector<GsVertex> vertices;
    std::string scenePath(plyPath);
    std::string ext = scenePath.substr(scenePath.find_last_of('.'));
    std::transform(ext.begin(), ext.end(), ext.begin(), ::tolower);

    bool parseOk;
    if (ext == ".spz") {
        parseOk = ParseSpzFile(scenePath, vertices);
    } else {
        parseOk = ParsePlyFile(scenePath, vertices);
    }

    if (!parseOk || vertices.empty()) {
        fprintf(stderr, "GsRenderer: failed to parse scene file: %s\n", plyPath);
        return false;
    }

    numGaussians_ = (uint32_t)vertices.size();
    numPrefixSumIter_ = (uint32_t)std::ceil(std::log2((double)numGaussians_));
    if (numPrefixSumIter_ == 0) numPrefixSumIter_ = 1;

    // Build compact pick data for CPU raycast (20 bytes/gaussian)
    pickData_.resize(numGaussians_);
    for (uint32_t i = 0; i < numGaussians_; i++) {
        const auto& v = vertices[i];
        pickData_[i].px = v.position[0];
        pickData_[i].py = v.position[1];
        pickData_[i].pz = v.position[2];
        pickData_[i].maxScale = std::max({v.scale_opacity[0], v.scale_opacity[1], v.scale_opacity[2]});
        pickData_[i].opacity = v.scale_opacity[3];
    }

    // Reject far-flung "floater" outlier splats from the pick set. Training
    // artifacts (e.g. KAWS FAMILY.spz carries ~3.4 % of its splats scattered
    // out to ±240 while the model itself spans only ~±15) are culled in the
    // render by the ZDP-relative far plane, so the user never sees them — but
    // the CPU raycast in pickGaussian() would still hit them, letting a
    // double-click focus land on an invisible splat far outside the model.
    // Drop any splat lying beyond a generous margin around the robust
    // [p2, p98] per-axis core. Clean scenes keep every splat because the
    // margin comfortably exceeds their true extent. This also tightens the
    // auto-fit bounds helpers, which share pickData_.
    if (numGaussians_ >= 1024) {
        const float kPickOutlierMargin = 3.0f;  // × half the p2–p98 range
        float keepMin[3], keepMax[3];
        std::vector<float> coord(numGaussians_);
        for (int axis = 0; axis < 3; axis++) {
            for (uint32_t i = 0; i < numGaussians_; i++) {
                const auto& g = pickData_[i];
                coord[i] = (axis == 0) ? g.px : (axis == 1) ? g.py : g.pz;
            }
            size_t loIdx = (size_t)(0.02f * (float)(numGaussians_ - 1));
            size_t hiIdx = (size_t)(0.98f * (float)(numGaussians_ - 1));
            if (hiIdx <= loIdx) hiIdx = loIdx + 1;
            std::nth_element(coord.begin(), coord.begin() + loIdx, coord.end());
            float lo = coord[loIdx];
            std::nth_element(coord.begin() + loIdx + 1, coord.begin() + hiIdx, coord.end());
            float hi = coord[hiIdx];
            float center = 0.5f * (lo + hi);
            float halfRange = 0.5f * (hi - lo);
            keepMin[axis] = center - kPickOutlierMargin * halfRange;
            keepMax[axis] = center + kPickOutlierMargin * halfRange;
        }
        size_t kept = 0;
        for (uint32_t i = 0; i < numGaussians_; i++) {
            const auto& g = pickData_[i];
            if (g.px < keepMin[0] || g.px > keepMax[0] ||
                g.py < keepMin[1] || g.py > keepMax[1] ||
                g.pz < keepMin[2] || g.pz > keepMax[2]) continue;
            pickData_[kept++] = g;
        }
        size_t dropped = (size_t)numGaussians_ - kept;
        pickData_.resize(kept);
        if (dropped > 0) {
            printf("GsRenderer: pick-set outlier rejection dropped %zu/%u floaters "
                   "(keep box [%.2f,%.2f]x[%.2f,%.2f]x[%.2f,%.2f])\n",
                   dropped, numGaussians_,
                   keepMin[0], keepMax[0], keepMin[1], keepMax[1],
                   keepMin[2], keepMax[2]);
        }
    }

    // Cache the (outlier-trimmed) scene bbox for the per-eye sort-depth
    // quantization range. One O(N) pass at load, reused every renderEye.
    sceneBBoxValid_ = getSceneBBox(sceneBBoxMin_, sceneBBoxMax_);

    // Create compute pipelines (first time only — they don't depend on scene size)
    if (pipePrecompCov3d_ == VK_NULL_HANDLE) {
        if (!createPipelines()) {
            fprintf(stderr, "GsRenderer: failed to create pipelines\n");
            return false;
        }
    }

    // Create GPU buffers
    if (!createBuffers()) {
        fprintf(stderr, "GsRenderer: failed to create buffers\n");
        return false;
    }

    // Upload vertex data via staging buffer
    if (!gsUploadBuffer(device_, physDevice_, queue_, cmdPool_,
                        vertexBuffer_, vertices.data(),
                        (VkDeviceSize)vertices.size() * sizeof(GsVertex))) {
        fprintf(stderr, "GsRenderer: failed to upload vertex data\n");
        return false;
    }

    // Create descriptor sets (must be after buffer creation)
    if (!createDescriptorSets()) {
        fprintf(stderr, "GsRenderer: failed to create descriptor sets\n");
        return false;
    }

    // One-time: transition render image to GENERAL layout
    {
        VkCommandBufferAllocateInfo ai = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO};
        ai.commandPool = cmdPool_;
        ai.level = VK_COMMAND_BUFFER_LEVEL_PRIMARY;
        ai.commandBufferCount = 1;
        VkCommandBuffer cmd;
        vkAllocateCommandBuffers(device_, &ai, &cmd);
        VkCommandBufferBeginInfo bi = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO};
        bi.flags = VK_COMMAND_BUFFER_USAGE_ONE_TIME_SUBMIT_BIT;
        vkBeginCommandBuffer(cmd, &bi);

        VkImageMemoryBarrier imb = {VK_STRUCTURE_TYPE_IMAGE_MEMORY_BARRIER};
        imb.oldLayout = VK_IMAGE_LAYOUT_UNDEFINED;
        imb.newLayout = VK_IMAGE_LAYOUT_GENERAL;
        imb.srcQueueFamilyIndex = VK_QUEUE_FAMILY_IGNORED;
        imb.dstQueueFamilyIndex = VK_QUEUE_FAMILY_IGNORED;
        imb.image = renderImage_.image;
        imb.subresourceRange = {VK_IMAGE_ASPECT_COLOR_BIT, 0, 1, 0, 1};
        imb.srcAccessMask = 0;
        imb.dstAccessMask = VK_ACCESS_SHADER_WRITE_BIT;
        vkCmdPipelineBarrier(cmd, VK_PIPELINE_STAGE_TOP_OF_PIPE_BIT,
            VK_PIPELINE_STAGE_COMPUTE_SHADER_BIT, 0, 0, nullptr, 0, nullptr, 1, &imb);

        vkEndCommandBuffer(cmd);
        VkSubmitInfo si = {VK_STRUCTURE_TYPE_SUBMIT_INFO};
        si.commandBufferCount = 1;
        si.pCommandBuffers = &cmd;
        vkQueueSubmit(queue_, 1, &si, VK_NULL_HANDLE);
        vkQueueWaitIdle(queue_);
        vkFreeCommandBuffers(device_, cmdPool_, 1, &cmd);
    }

    // Dispatch one-time precomp_cov3d
    dispatchPrecompCov3d();

    loadedScenePath_ = plyPath;
    sceneLoaded_ = true;

    printf("GsRenderer: scene loaded: %s (%u gaussians)\n", plyPath, numGaussians_);
    return true;
}

